#ifndef	__BSP_I2C_H_
#define	__BSP_I2C_H_
/* Includes-----------------------------------------------------------------------------------*/
#include "sysconfig.h"
/* Private define-----------------------------------------------------------------------------*/
/* Private typedef----------------------------------------------------------------------------*/
/* Private macro------------------------------------------------------------------------------*/
/* Private variables--------------------------------------------------------------------------*/
/* Private function prototypes----------------------------------------------------------------*/
/* Private functions--------------------------------------------------------------------------*/
/*****************************************************************************
* Function     : IIC_Init
* Description  : I2C初始化
* Input        : void  
* Output       : None
* Return       : 
* Note(s)      : 
* Contributor  : 2018年5月25日
*****************************************************************************/
void IIC_Init(void);

/*****************************************************************************
* Function     : IIC_Start
* Description  : I2C起始条件:SDA在SCL为高电平期间产生一个下降沿
* Input        : void  
* Output       : None
* Return       : 
* Note(s)      : 
* Contributor  : 2018年5月25日
*****************************************************************************/
void IIC_Start(void);

/*****************************************************************************
* Function     : IIC_Stop
* Description  : I2C停止条件:SDA在SCL为高电平期间产生了一个上升沿
* Input        : void  
* Output       : None
* Return       : 
* Note(s)      : 
* Contributor  : 2018年5月25日
*****************************************************************************/
void IIC_Stop(void);


/*****************************************************************************
* Function     : IIC_Ack
* Description  : 接收8位数据后产生一个ACK(发送SDA=0),SDA只能在SCL=0是发生变化，
                 否则将产生了起始条件或者停止条件
* Input        : void  
* Output       : None
* Return       : 
* Note(s)      : 
* Contributor  : 2018年5月25日
*****************************************************************************/
void IIC_Ack(void);

/*****************************************************************************
* Function     : IIC_NAck
* Description  : 接收到数据后不产生一个ACK(发送SDA=1)
* Input        : void  
* Output       : None
* Return       : 
* Note(s)      : 
* Contributor  : 2018年5月25日
*****************************************************************************/
void IIC_NAck(void);

/*****************************************************************************
* Function     : IIC_SendByte
* Description  : I2C发送一个字节数据,按位发送，先发送MSB
* Input        : INT8U val  
* Output       : None
* Return       : 
* Note(s)      : 
* Contributor  : 2018年5月25日
*****************************************************************************/
INT8U IIC_SendByte(INT8U val);

/*****************************************************************************
* Function     : IIC_RecvByte
* Description  : I2C接收一个字节
* Input        : void  
* Output       : None
* Return       : 
* Note(s)      : 
* Contributor  :2018年5月25日
*****************************************************************************/
INT8U IIC_RecvByte(void);

#endif	//__BSP_I2C_H_

